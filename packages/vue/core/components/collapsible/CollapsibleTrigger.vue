<script lang="ts">
export interface CollapsibleTriggerProps extends ArkCollapsibleTriggerProps, ThemeNoCrafts {
  class?: HTMLAttributes['class']
  indicator?: boolean
  ui?: {
    root?: HTMLAttributes['class']
    indicator?: HTMLAttributes['class']
  }
}
</script>

<script setup lang="ts">
import type { CollapsibleTriggerProps as ArkCollapsibleTriggerProps } from '@ark-ui/vue/collapsible'
import type { ThemeNoCrafts } from '@rui-ark/vue/providers/theme'
import type { HTMLAttributes } from 'vue'
import { useForwardProps } from '@ark-ui/vue'
import { Collapsible, useCollapsibleContext } from '@ark-ui/vue/collapsible'
import { useTheme } from '@rui-ark/vue/composables/useTheme'
import { ChevronDown } from 'lucide-vue-next'
import { computed } from 'vue'

const {
  class: propsClass,
  theme: propsTheme,
  indicator = true,
  ui,
  ...props
} = defineProps<CollapsibleTriggerProps>()
const forwarded = useForwardProps(props)
const context = useCollapsibleContext()

// theme
const theme = useTheme(() => propsTheme)
const crafts = computed(() => theme.value.crafts.tvCollapsible())
</script>

<template>
  <Collapsible.Trigger
    v-bind="forwarded"
    :class="crafts.trigger({ class: [ui?.root, propsClass], ...theme })"
  >
    <slot v-bind="{ open: context.open, visible: context.visible }" />
    <slot
      name="indicator"
      v-bind="{ open: context.open, visible: context.visible }"
    >
      <Collapsible.Indicator
        v-if="indicator"
        :class="crafts.indicator({ class: ui?.indicator, ...theme })"
      >
        <ChevronDown :style="{ width: '1lh', height: '1lh' }" />
      </Collapsible.Indicator>
    </slot>
  </Collapsible.Trigger>
</template>
