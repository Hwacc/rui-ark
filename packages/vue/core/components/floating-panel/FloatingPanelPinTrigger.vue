<script lang="ts">
export interface FloatingPanelPinTriggerProps extends PolymorphicProps, Theme {
  class?: HTMLAttributes['class']
}
</script>

<script setup lang="ts">
import type { PolymorphicProps } from '@ark-ui/vue/factory'
import type { Theme } from '@rui-ark/vue/providers/theme'
import type { HTMLAttributes } from 'vue'
import { useForwardProps } from '@ark-ui/vue'
import { ark } from '@ark-ui/vue/factory'
import { tvFloatingPanel } from '@rui-ark/themes/crafts/core/floating-panel'
import { useTheme } from '@rui-ark/vue/composables/useTheme'
import { Pin, PinOff } from 'lucide-vue-next'
import { injectFloatingPanelAppearanceContext } from './floating-panel-appearance-context'

const {
  class: propsClass,
  theme: propsTheme,
  ...props
} = defineProps<FloatingPanelPinTriggerProps>()
const emit = defineEmits<{ click: [event: MouseEvent] }>()

const forwarded = useForwardProps(props)
const { pinned, setPinned } = injectFloatingPanelAppearanceContext()

function handleClick(e: MouseEvent) {
  emit('click', e)
  if (e.defaultPrevented)
    return
  setPinned(!pinned.value)
}

// theme
const theme = useTheme(() => propsTheme)
const { trigger } = tvFloatingPanel()
</script>

<template>
  <ark.button
    v-bind="forwarded"
    :class="trigger({ class: [propsClass], ...theme })"
    @click="handleClick"
  >
    <slot v-bind="{ pinned, setPinned }">
      <PinOff v-if="pinned" :style="{ width: '1lh', height: '1lh' }" />
      <Pin v-else :style="{ width: '1lh', height: '1lh' }" />
    </slot>
  </ark.button>
</template>
